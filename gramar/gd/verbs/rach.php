<h3 id="motion">1. <em>rach</em> as a verb of motion</h3>
<p>A material entity traverses a spatial trajectory.</p>
<p>Subject denotes a material entity, e.g. person, animal, ...</p>
<p>Complement denotes a trajectory, i.e. is a <a href="../../xx/pathExpressions.php">path expression</a>.</p>
<p>Motion is away from the deictic centre (‘here’).</p>
