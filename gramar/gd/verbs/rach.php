<h3 id="aspectual">2. <em>rach</em> as an aspectual/modal auxiliary verb – <em>rach do dhèanamh rud</em></h3>

<h4>2.1. Future tense: <em>bi a’ dol a dhèanamh rud</em></h4>
<p>No movement.</p>
<p>Examples from reported speech:</p>
<ul>
  <li><strong>“Tha mi <mark>a’ dol a thoirt</mark> dhut aon chothrom dearbhadh gur e duine sìobhalta a tha annad.”</strong><br/> 
    <small class="text-muted">“I’m going to give you one chance to prove that you’re a decent person.” [#2 76]</small></li>
  <li><strong>“A bheil thu a’ tuigsinn gu bheil thu <mark>dol a’ [sic] bhàsachadh</mark>?”</strong><br/>
    <small class="text-muted">“Do you understand that you are going to die?” [#2 6]</small></li>
  <li><strong>“Chuir mi peanta geal air an fheadhainn a tha thu <mark>a’ dol a ghearradh</mark>.”</strong><br/>
    <small class="text-muted">“I have put white paint on the ones that you’re going to cut down.”[#2 30]</small></li>
  <li><strong>“Chan eil sibh <mark>a’ dol a chreidsinn</mark> seo.”</strong><br/>
    <small class="text-muted">“You are not going to believe this.” [#2 48]</small></li>
</ul>
<p>Examples from past tense narrative:</p>
<ul>
  <li><strong>Bha e <mark>dol a phòsadh</mark> na h-ìghne bu bhrèagha bha san sgìre</strong><br/> 
    <small class="text-muted">‘He was going to marry the most beautiful girl in the district.’ [#2 83]</small></li>
  <li><strong>Chuir Aonghas a mheur air a bheul, a’ comharrachadh gu robh e <mark>dol a shealltainn</mark> dhomh rud dìomhair.</strong><br/> 
    <small class="text-muted">‘Angus put his finger on his mouth, signalling that he was going to show me something secret.’ [#2 32]</small></li>
  <li><strong>Thuig Eubha nach robh i <mark>a’ dol a dhèanamh</mark> adhartais air an taca sa.</strong><br/> 
    <small class="text-muted">‘Eve understood that she wasn’t going to make any progress with this approach.’ [#2 88]</small></li>
  <li><strong>Bha a h-uile coltas gu robh i <mark>a’ dol a bhriseadh</mark>.</strong><br/> 
    <small class="text-muted">‘It appeared that it was going to break.’ [#2 45]</small></li>  
</ul>
<h4>2.2. Motion with intention:</h4>
<p>Non-progressive. Motion with a particular intention or purpose.</p>
<p>Examples from reported speech:</p>
<ul>
  <li><strong>“Nach bu chòir dhuit a <mark>dhol a shealltainn</mark> a bheil d’ athair a’ tighinn?”</strong><br/> 
    <small class="text-muted">“Shouldn’t you go and see if your father is coming?” [#2 123]</small></li>
    <li><strong>“Chan eil cead againn a <mark>dhol a choimhead</mark> air a’ chiste.”</strong><br/> 
    <small class="text-muted">“We didn’t have permission to go and look at the chest.” [#2 38]</small></li>
</ul>
<p>Examples from past tense narrative:</p>
<ul>
  <li><strong><mark>Chaidh e a dh’fheuchainn</mark> am faigheadh e iasg.</strong><br/> 
    <small class="text-muted">‘He had gone to try and get fish.’ [#2 123]</small></li>
