<h1>airson</h1>
<p><strong>airson</strong> <span class="text-muted">‘for (the sake of)’</span> is a compound preposition, traditionally spelled as two separate words <strong>air son</strong>.</p>
<hr/>
<p>One recent use of airson is to introduce a time adverbial expressing the duration of a state or activity. 
In these cases, <strong>airson</strong> governs an <em>indefinite</em> time noun, and modifies a verb denoting a (non-bounded) state or activity. For example –</p>
