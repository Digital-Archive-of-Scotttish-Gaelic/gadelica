<h1>fad</h1>
<p><strong>fad</strong> <span class="text-muted">‘length’</span> is an abstract (masculine) noun, 
derived from the adjective <strong><a href="fada.php">fada</a></strong> <span class="text-muted">‘long’</span>.</p>
<p>Forms of <strong>fad</strong> are also used to introduce a temporal adverbial phrase or clause, as if it were a preposition or conjunction.</p>
<hr/>
<p>The main uses of <strong>fad</strong> are –</p>
<ol>
  <li>As an abstract noun meaning <span class="text-muted">‘length’</span>, the quality of being <span class="text-muted">‘long’</span> <small><a data-toggle="modal" data-target="#noun" href="#">[more]</a></small></li>
  <li>As a pseudo-preposition, governing a noun denoting a time interval, and introducing a temporal adverbial phrase of <em>duration</em> –
    <ol type="a">
      <li><em>indefinite</em> – <mark>fad bliadhna</mark> <span class="text-muted">‘for a year’</span> <small><a data-toggle="modal" data-target="#preposition-indefinite" href="#">[more]</a></small></li>
      <li><em>definite</em> – <mark>fad na bliadhna</mark> <span class="text-muted">‘all year (long)’</span> <small><a data-toggle="modal" data-target="#preposition-definite" href="#">[more]</a></small></li>
    </ol>
  </li>
  <li>As a pseudo-conjunction <strong>fhad ’s a ...</strong>, governing an imperfective clause, and introducing a temporal adverbial clause of duration –
    <ol type="a">
      <li>modifying an <em>imperfective</em> – <span class="text-muted">‘as long as’</span>, <span class="text-muted">‘(all the) while’</span>, 
      e.g. <mark>fhad ’s a bha mi thall thairis, bha i ag obair</mark> <span class="text-muted">‘all the while I was abroad, she was working’</span> <small><a data-toggle="modal" data-target="#conjunction-imperfective" href="#">[more]</a></small></li>
      <li>modifying a <em>perfective</em> – <span class="text-muted">‘while’</span>, 
      e.g. <mark>fhad ’s a bha mi thall thairis, phòs i</mark> <span class="text-muted">‘while I was abroad, she got married’</span> <small><a data-toggle="modal" data-target="#conjunction-perfective" href="#">[more]</a></small></li>
    </ol>
  </li>
</ol>

