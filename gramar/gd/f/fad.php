<h1>fad</h1>
<p><strong>fad</strong> is a noun with the basic meaning <span class="text-muted">‘length’</span>, derived from the adjective <strong>fada</strong> <span class="text-muted">‘long’</span>.</p>
<p>It can also be used to introduce a time adverbial, as a kind of preposition or conjunction.</p>
<hr/>
<p>The main uses of <strong>fad</strong> are –</p>
<ul>
  <li>as noun meaning <span class="text-muted">'length'</span>, the quality of being <span class="text-muted">‘long’</span> <small><a data-toggle="modal" data-target="#noun" href="#">[more]</a></small></li>
  <li>as a kind of preposition introducing a time adverbial (genitive case)
    <ul>
      <li>governing an <em>indefinite</em> time noun, e.g. <mark>fad bliadhna</mark> <span class="text-muted">‘for a year’</span> <small><a data-toggle="modal" data-target="#preposition-indefinite" href="#">[more]</a></small></li>
      <li>governing a <em>definite</em> time noun, e.g. <mark>fad na bliadhna</mark> <span class="text-muted">‘all year (long)’</span> <small><a data-toggle="modal" data-target="#preposition-definite" href="#">[more]</a></small></li>
    </ul>
  <li>as a kind of conjunction introducing a time adverbial, e.g. <mark>fhad ’s a bha mi a-muigh</mark> <span class="text-muted">‘while / as long as I was out’</span> <small><a data-toggle="modal" data-target="#conjunction" href="#">[more]</a></small></li>
</ul>

