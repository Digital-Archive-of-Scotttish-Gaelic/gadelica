<h1>gus</h1>
<p><strong>gus</strong> is a pseudo-conjunction governing one of the interrogative verb particles <strong>an</strong> or <strong>nach</strong>, 
and corresponding to the English conjunctions <span class="text-muted">‘until’</span>, <span class="text-muted">‘in order that’</span>.</p>
<hr/>
<p><strong>gus (an)</strong> can be used to introduce a time adverbial expressing the end-point of a state or activity in terms of some event or action. 
