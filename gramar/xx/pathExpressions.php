<h1>Path expressions</h1>
<p>A path expression denotes one-dimensional directed space, along which an object may travel.</p>



<p>Any (non-proximal) path expression can be the complement of the verb <strong><a href="../gd/verbs/rach.php">rach</a></strong> in its ‘physical motion’ sense.</p>
<hr/>
