<h3>2. Path prepositions</h3>
<p>to-paths</p>
<ul>
  <li><strong><a href="../gd/prepositions/gu.php">gu</a></strong> ‘(up) to’
    <ul>
      <li><strong>Chaidh e <mark>gu</mark> doras an taighe.</strong><br/> 
